package ListaExercicio;

public class Exercicio5 {

	public void Condicao(int numero) {
		
        if (numero >= 0) {
            int dobro = numero * 2;
            System.out.println("O dobro do número " + numero + " é " + dobro);
        } else {
            int triplo = numero * 3;
            System.out.println("O triplo do número " + numero + " é " + triplo);
        }
    }
	
}

