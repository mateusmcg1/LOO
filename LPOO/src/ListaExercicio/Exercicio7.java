package ListaExercicio;

public class Exercicio7 {
	
	 public void  Calcula(int numero) {

	        int resultado;

	        if (numero % 2 == 0) {
	            // número par, soma 5
	            resultado = numero + 5;
	        } else {
	            // número ímpar, soma 8
	            resultado = numero + 8;
	        }

	        System.out.println("Resultado da soma: " + resultado);
;
	 }
	 
	 
	 

}
